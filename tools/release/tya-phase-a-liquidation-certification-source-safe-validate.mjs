import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here=path.dirname(fileURLToPath(import.meta.url));
const root=path.resolve(here,'../..');
const args=process.argv.slice(2);
const outIndex=args.indexOf('--out');
const outDir=outIndex>=0?path.resolve(root,args[outIndex+1]):path.join(root,'.tmp/liquidation-certification-source-safe');
const read=rel=>JSON.parse(fs.readFileSync(path.join(root,rel),'utf8'));
const payment=read('backend/contracts/phase-a-liquidation-payment-source-safe-projection-v1.json');
const cert=read('backend/contracts/phase-a-certification-carryover-source-safe-projection-v1.json');
const snapshot=read('backend/config/phase-a-liquidation-certification-projection-summary.source-safe.json');
const blockers=[];
const expect=(ok,id)=>{if(!ok)blockers.push(id);};
expect(snapshot.liquidation.records===572,'liquidation_records');
expect(snapshot.liquidation.historicalThroughMay===528,'historical_through_may');
expect(snapshot.liquidation.june===44,'june_records');
expect(snapshot.liquidation.juneGT===34&&snapshot.liquidation.juneHN===10,'june_country_split');
expect(snapshot.liquidation.juneQ1===18&&snapshot.liquidation.juneQ2===22&&snapshot.liquidation.juneUnknownQuincena===4,'june_quincena_split');
expect(snapshot.liquidation.paidConfirmed===0,'no_false_paid');
expect(snapshot.liquidation.pendingHistoricalPaymentMatch===528,'historical_payment_match_pending');
expect(snapshot.liquidation.junePendingFinancialSource===40&&snapshot.liquidation.juneConflicts===4,'june_payment_control');
expect(snapshot.liquidation.amountComplete+snapshot.liquidation.amountPartial===snapshot.liquidation.records,'amount_partition');
expect(snapshot.liquidation.loteEligible===0,'no_lote_eligible_without_finance');
expect(snapshot.certification.shoppers===213,'certification_shoppers');
expect(snapshot.certification.sourceRecords===0&&snapshot.certification.eligibleConfirmed===0&&snapshot.certification.pendingSource===213,'no_false_certification');
expect(snapshot.reviewQueue.total===snapshot.reviewQueue.liquidation+snapshot.reviewQueue.payment+snapshot.reviewQueue.certification,'review_queue_total');
expect(snapshot.sourceStatus.financial==='pending_financial_source','financial_source_pending');
expect(snapshot.sourceStatus.certification==='pending_certification_source','certification_source_pending');
expect(snapshot.sourceSafe===true&&snapshot.imported===false&&snapshot.production===false&&snapshot.writes===false&&snapshot.providers===false,'safe_state');
expect(payment.requiredForPaid.includes('paidAt')&&payment.requiredForPaid.includes('auditRef'),'payment_required_fields');
expect(cert.requiredForCarryover.includes('reviewedBy')&&cert.requiredForCarryover.includes('sourceRef'),'cert_required_fields');
const result={ok:blockers.length===0,generatedAt:new Date().toISOString(),contracts:[payment.contractId,cert.contractId],snapshot,blockers};
fs.mkdirSync(outDir,{recursive:true});
fs.writeFileSync(path.join(outDir,'phase-a-liquidation-certification-source-safe-report.json'),JSON.stringify(result,null,2)+'\n','utf8');
fs.writeFileSync(path.join(outDir,'phase-a-liquidation-certification-source-safe-report.md'),`# Phase A liquidation/payment + certification source-safe\n\nVerdict: **${result.ok?'PASS':'HOLD'}**\n\n- Liquidation candidates: ${snapshot.liquidation.records}\n- Paid confirmed: ${snapshot.liquidation.paidConfirmed}\n- June controls: ${snapshot.liquidation.june}\n- Certification shoppers pending source: ${snapshot.certification.pendingSource}\n- ReviewQueue candidates: ${snapshot.reviewQueue.total}\n- Blockers: ${blockers.length}\n`,'utf8');
console.log(JSON.stringify(result,null,2));
if(!result.ok)process.exit(1);
